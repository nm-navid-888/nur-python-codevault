# List (Mutable)

fruits = ["apple", "banana"]
fruits.append("cherry")
print(fruits)  # ['apple', 'banana', 'cherry']

# Dictionary (Mutable)

student = {"name": "Nur", "age": 22}
student["grade"] = "A"
print(student)  # {'name': 'Nur', 'age': 22, 'grade': 'A'}

# Set (Mutable)

colors = {"red", "blue"}
colors.add("green")
print(colors)  # {'red', 'blue', 'green'}


